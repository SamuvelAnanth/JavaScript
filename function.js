function function1(){   //No Argument and No ReturnType
    console.log("Welcome Samuvel");
}
function1();
function function2(stu_name){     //Argument with No ReturnType
    console.log("Welcome "+stu_name);
}
function2("Samuvel");
function function3(){           //No Argument with ReturnType
    return "Welcome You All"
}
console.log(function3());
function function4(a){          //Argument with ReturnType
    return "The Value of A is : "+a;
}
console.log(function4(10));